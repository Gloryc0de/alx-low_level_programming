#include <stdio.h>

/**
* add - adds two integer values
* @a: integer a
 *@b: integer b
 *Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtract two integer values
* @a: integer a
* @b: integer b
* Return: result
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - multiplies two integer values
* @a: integer a
* @b: integer b
* Return: result
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
* div - divides two integer values
* @a: integer a
* @b: integer b
* Return: result
*/

int div(int a, int b)
{

	return (a / b);
}

/**
* mod - modules of two integer value
* @a: integer a
* @b: integer b
* Return: result
*/

int mod(int a, int b)
{
	return (a % b);
}
