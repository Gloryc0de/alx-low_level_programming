#include "main.h"
/**
 * print_alphabet - print alphabet in lower case followed by a new line
 *
 *
 *
 * Return: Always 0.
 *
 */

void print_alphabet(void)
{
	int i;
	char ch;

	for (i = 1; i <= 10; i++)
	{
		for (ch = 'a'; ch <= 'z'; ch++)

		{
			_putchar(ch);
		{

		_putchar('\n');
		}
		}
	}
	
}
